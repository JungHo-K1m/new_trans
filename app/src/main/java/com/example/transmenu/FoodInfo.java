package com.example.transmenu;

public class FoodInfo {
    private String title;
    private String url;


    public void setNoTitle(String title) {
        this.title = title;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNoTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}
